#include "ft_printf.h"

// ============================================================================
// ------------------------------ print_char ----------------------------------
// ============================================================================

// writes a char on the stdout and returns the count of printed char (1)

int	print_char(int c)
{
	int	count;

	count = 0;
	count += write(1, &c, 1);
	return (count);
}
